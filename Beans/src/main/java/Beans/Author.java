package Beans;

public class Author {
  private String Name;
  private String Phone;
  private String Age;
  public void setName(String Name) {
		Name = Name;
	}
  public String getName(){
    return Name;
  }
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}


}
